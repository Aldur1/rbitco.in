<h4>What is Bitcoin?</h4>
<p>Buckle up, because that's not an easy or short answer.  Simply, Bitcoin is a peer-to-peer decentralized crypto-currency.  There is no central issuer of bitcoins.  All the bitcoin clients work together to maintain a record of every transaction in what's known as the blockchain.  There are around 10,000,000 bitcoins in existence as of this writing on 10/4/2012.  The algorithm that controls bitcoin generation ensures that there will never be more than 21,000,000 in existence.</p>
<p>As of today the largest Bitcoin exchange is MtGox.com, transacting an average of $400k USD worth of bitcoin trades every day.  Bitcoin is a currency by definition:  
<strong>It is a store of value,  merchants accept it, and it can be easily transferred.</strong>  
In its relatively short history, the price of bitcoin has fluctuated wildly.  
Take a look at a lifetime BTC/USD chart from bitcoincharts.com, and watch another Micon-video on the lifetime price graph of BTC to understand why the price moved as it did.</p>
